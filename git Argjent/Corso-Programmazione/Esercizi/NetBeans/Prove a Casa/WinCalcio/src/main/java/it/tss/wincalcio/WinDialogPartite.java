    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int index = tblPartite.getSelectedRow();
        if (index == -1) {
            return;
        }
        int input = JOptionPane.showConfirmDialog(null,
                "Confermi l'eliminazione?", "ELIMINA Partita", JOptionPane.OK_CANCEL_OPTION);
        if (input == 0) {
            listapartite.remove(index);
            salvaCSVPt();
            salvaCSVClass();
            refreshTable();
            pulisci();
            lblMsg.setText("Partita eliminata correttamente");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

