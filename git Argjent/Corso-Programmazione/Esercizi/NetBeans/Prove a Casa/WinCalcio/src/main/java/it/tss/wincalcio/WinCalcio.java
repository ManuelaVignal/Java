    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int index = tblElencoSq.getSelectedRow();
        if (index == -1) {
            return;
        }
        int input = JOptionPane.showConfirmDialog(null,
                "Confermi l'eliminazione?", "ELIMINA Squadra", JOptionPane.OK_CANCEL_OPTION);
        if (input == 0) {
            listasquadre.remove(index);
            salvaCSVSq();
            refreshTable();
            pulisci();
            lblMsg.setText("Squadra eliminata correttamente");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

