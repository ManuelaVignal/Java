    private void caricaSquadre() {
        try {
            File fi = new File("");
            String pf = fi.getCanonicalPath();
            File filecsv = new File(pf + "/Calcio/squadre.csv");
            Scanner lettore = new Scanner(filecsv);
            boolean primariga = true;
            while (lettore.hasNextLine()) {
                String riga = lettore.nextLine();
                if (!primariga) {
                    String[] dati = riga.split(";");
                    String nmsq = dati[0];
                    String nmsede = dati[1];
                    Squadra sq = new Squadra(nmsq, nmsede);
                    listasquadre.add(sq);
                }
                primariga = false;
            }

        } catch (Exception e) {

        }

    }

    private void caricaPartite() {
        try {
            File fi = new File("");
            String pf = fi.getCanonicalPath();
            File filecsv = new File(pf + "/Calcio/partite.csv");
            Scanner lettore = new Scanner(filecsv);
            boolean primariga = true;
            while (lettore.hasNextLine()) {
                String riga = lettore.nextLine();
                if (!primariga) {
                    String[] dati = riga.split(";");
                    int g = Integer.parseInt(dati[0]);
                    String nsq1 = dati[1];
                    String nsq2 = dati[2];
                    int gol1 = Integer.parseInt(dati[3]);
                    int gol2 = Integer.parseInt(dati[4]);
                    Partita pt = new Partita(g, nsq1, nsq2, gol1, gol2);
                    listapartite.add(pt);
                }
                primariga = false;
            }

        } catch (Exception e) {

        }
    }
    
    private void caricaClassifica(){
        try {
            File fi = new File("");
            String pf = fi.getCanonicalPath();
            File filecsv = new File(pf + "/Calcio/classifica.csv");
            Scanner lettore = new Scanner(filecsv);
            boolean primariga = true;
            while (lettore.hasNextLine()) {
                String riga = lettore.nextLine();
                if (!primariga) {
                    String[] dati = riga.split(";");
                    String sq = dati[0];
                    int p = Integer.parseInt(dati[1]);
                    Classifica cl = new Classifica(sq, p);
                    WinDialogPartite.classifica.add(cl);
                }
                primariga = false;
            }

        } catch (Exception e) {

        }
    }

    private void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tblElencoSq.getModel();
        model.setRowCount(0);
        int ncol = model.getColumnCount();
        Object[] rowData = new Object[ncol];
        for (Squadra sq : listasquadre) {
            rowData[0] = sq.getNome();
            rowData[1] = sq.getSede();
            model.addRow(rowData);
        }
    }

    private void pulisci() {
        txNome.setText("");
        txSede.setText("");
    }

    private void salvaCSVSq() {
        String intestazione = "nome;sede\n";
        String dati = "";
        for (int i = 0; i < listasquadre.size(); i++) {
            Squadra sq = listasquadre.get(i);
            String riga = sq.getCSVSq();
            dati += riga;
        }
        String txfile = intestazione + dati;
        try {
            File fi = new File("");
            String pf = fi.getCanonicalPath();
            FileWriter myWriter = new FileWriter(pf + "/Calcio/squadre.csv");
            myWriter.write(txfile);
            myWriter.close();
        } catch (Exception e) {
            lblMsg.setText("Errore di scrittura file squadre.csv");
        }
    }

