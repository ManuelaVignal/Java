    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        int g = Integer.parseInt(String.valueOf(spGiornata.getValue()));
        String sq1 = txSquadra1.getText();
        String sq2 = txSquadra2.getText();
        int gol1 = Integer.parseInt(String.valueOf(spGol1.getValue()));
        int gol2 = Integer.parseInt(String.valueOf(spGol2.getValue()));
        Partita pt = new Partita(g, sq1, sq2, gol1, gol2);
        if (tblPartite.getSelectedRow() == -1 && verifica(sq1, sq2)) {
            listapartite.add(pt);
            Squadra squadra1 = new Squadra();
            int p1 = squadra1.punteggio(sq1);
            Squadra squadra2 = new Squadra();
            int p2 = squadra2.punteggio(sq2);
            Classifica cl1 = new Classifica(sq1, p1);
            Classifica cl2 = new Classifica(sq2, p2);
            classifica.add(cl1);
            classifica.add(cl2);
            lblMsg.setText("Partita aggiunta correttamente!");
        } else if (tblPartite.getSelectedRow() != -1 && verifica(sq1, sq2)) {
            int index = tblPartite.getSelectedRow();
            Squadra squadra1 = new Squadra();
            int p1 = squadra1.punteggio(sq1);
            Squadra squadra2 = new Squadra();
            int p2 = squadra2.punteggio(sq2);
            Classifica cl1 = new Classifica(sq1, p1);
            Classifica cl2 = new Classifica(sq2, p2);
            classifica.add(cl1);
            classifica.add(cl2);
            listapartite.set(index, pt);
            lblMsg.setText("Partita aggiornata correttamente!");
        } else if (!verifica(sq1, sq2)) {
            lblMsg.setText("Una o pi√π squadre inserite non sono presenti nell'elenco squadre");
            return;
        }

        pulisci();
        salvaCSVPt();
        salvaCSVClass();
        classifica.clear();
        refreshTable();
        tblPartite.clearSelection();
    }//GEN-LAST:event_btnConfirmActionPerformed

