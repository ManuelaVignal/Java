    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        String nmsq = txNome.getText();
        String nmsede = txSede.getText();
        Squadra sq = new Squadra(nmsq, nmsede);
        if (tblElencoSq.getSelectedRow() == -1) {
            listasquadre.add(sq);
            lblMsg.setText("Squadra aggiunta correttamente!");
        } else {
            int index = tblElencoSq.getSelectedRow();
            listasquadre.set(index, sq);
        }
        pulisci();
        salvaCSVSq();
        refreshTable();
        tblElencoSq.clearSelection();
    }//GEN-LAST:event_btnConfirmActionPerformed

