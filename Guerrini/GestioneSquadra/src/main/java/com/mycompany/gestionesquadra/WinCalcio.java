    private boolean validaSquadra() {
        boolean ret = false;

        boolean okNome = !txNomeSquadra.getText().equals("");
        boolean okSede = !txSede.getText().equals("");

        if (okNome && okSede) {
            ret = true;
        }
        return ret;
    }

    private void salvaPartitaCSV() {
        // recupero un corso per volta
        // estraggo info tipo String
        // aggiungo info a un testo generico che poi setto in display
        String testoDisplay = "g;incasa;trsaferta;gol1;gol2\n";
        for (int i = 0; i < Elencopa.size(); i++) {
            // recupero un corso per volta
            Partita pa = Elencopa.get(i);
            String info = pa.getCSVInfo();
            testoDisplay += info;
        }
        try {
            // creo file corsi.txt
            FileWriter myWriter = new FileWriter("/home/tss/Partita/partita.csv");
            myWriter.write(testoDisplay);
            // ci scrivo dentro testoDisplay
            // lo chiudo se no si blocca
            myWriter.close();
        } catch (Exception e) {

        }
    }

    private void salvaPartita() {

        // aggiungo info a un testo generico che poi setto in displaysiCSV();
        String testoDisplay = "";
        for (int i = 0; i < Elencopa.size(); i++) {
            // recupero un prodotto per volta
            Partita pa = Elencopa.get(i); //get(i) sarebbe listacorsi[i] in un array normale;
            String info = pa.getInfo();
            testoDisplay += info;
        }
        try {
            // creo file corsi.txt
            FileWriter myWriter = new FileWriter("/home/tss/Partita/partita.txt");
            myWriter.write(testoDisplay);
            // ci scrivo dentro testoDisplay
            // lo chiudo se no si blocca
            myWriter.close();
        } catch (Exception e) {

        }
    }


