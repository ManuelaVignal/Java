    private void caricaDatiSquadra() {
        try {
            //aprire il file /tss/Scuola/dati.csv
            File filecsv = new File("/home/tss/Squadre/squadra.csv");
            //creo scanner per leggere una riga per volta
            Scanner lettore = new Scanner(filecsv);
            //estrarre una riga per volta
            boolean primariga = true;
            while (lettore.hasNextLine()) {
                String riga = lettore.nextLine();
                if (!primariga) {
                    String[] dati = riga.split(";");
                    //dati[0] nomecorso dati[1] durata etc...

                    String nome = dati[0];
                    String sede = dati[1];

                    Squadra s = new Squadra(nome, sede);

                    Eleconcosq.add(s);
                }
                primariga = false;
            }
            //righe del file
            //alla fine chiudo il file se no si blocca
        } catch (Exception e) {

        }
    }

    private void caricaDatiPartita() {
        try {
            //aprire il file /tss/Scuola/dati.csv
            File filecsv = new File("/home/tss/Squadre/squadra.csv");
            //creo scanner per leggere una riga per volta
            Scanner lettore = new Scanner(filecsv);
            //estrarre una riga per volta
            boolean primariga = true;
            while (lettore.hasNextLine()) {
                String riga = lettore.nextLine();
                if (!primariga) {
                    String[] dati = riga.split(";");
                    //dati[0] nomecorso dati[1] durata etc...

                    String squadra1 = dati[0];
                    String squadra2 = dati[1];
                    int gol1 = Integer.parseInt(dati[2]);
                    int gol2 = Integer.parseInt(dati[3]);
                    int giornata = Integer.parseInt(dati[4]);

                    Partita p = new Partita(squadra1, squadra2, gol1, gol2, giornata);

                    Elencopa.add(p);
                }
                primariga = false;
            }
            //righe del file
            //alla fine chiudo il file se no si blocca
        } catch (Exception e) {

        }
    }

    private void refreshLista() {
        DefaultListModel model = new DefaultListModel();
        for (int i = 0; i < Eleconcosq.size(); i++) {
            String nc = Eleconcosq.get(i).getNome_squadra();
            model.addElement(nc);
        }
        lstSquadra.setModel(model);

    }

    private void salvaSquadraCSV() {
        // recupero un corso per volta
        // estraggo info tipo String
        // aggiungo info a un testo generico che poi setto in display
        String testoDisplay = "nomesquadra;sede\n";
        for (int i = 0; i < Eleconcosq.size(); i++) {
            // recupero un corso per volta
            Squadra sq = Eleconcosq.get(i);
            String info = sq.getCSVInfo();
            testoDisplay += info;
        }
        try {
            // creo file corsi.txt
            FileWriter myWriter = new FileWriter("/home/tss/Squadra/squadre.csv");
            myWriter.write(testoDisplay);
            // ci scrivo dentro testoDisplay
            // lo chiudo se no si blocca
            myWriter.close();
        } catch (Exception e) {

        }
    }

    private void salvaSquadra() {

        // aggiungo info a un testo generico che poi setto in displaysiCSV();
        String testoDisplay = "";
        for (int i = 0; i < Eleconcosq.size(); i++) {
            // recupero un prodotto per volta
            Squadra sq = Eleconcosq.get(i); //get(i) sarebbe listacorsi[i] in un array normale;
            String info = sq.getInfo();
            testoDisplay += info;
        }
        try {
            // creo file corsi.txt
            FileWriter myWriter = new FileWriter("/home/tss/Squadra/squadre.txt");
            myWriter.write(testoDisplay);
            // ci scrivo dentro testoDisplay
            // lo chiudo se no si blocca
            myWriter.close();
        } catch (Exception e) {

        }
    }
