    private void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tblPartite.getModel();
        model.setRowCount(0);
        int ncol = model.getColumnCount();
        Object[] rowData = new Object[ncol];
        for (Partita pt : listapartite) {
            rowData[0] = pt.getGiornata();
            rowData[1] = pt.getSquadra1();
            rowData[2] = pt.getSquadra2();
            rowData[3] = pt.getGol1();
            rowData[4] = pt.getGol2();
            model.addRow(rowData);
        }
    }

    private void pulisci() {
        spGiornata.setValue(1);
        txSquadra1.setText("");
        txSquadra2.setText("");
        spGol1.setValue(0);
        spGol2.setValue(0);
        tblPartite.clearSelection();
    }

    private void salvaCSVPt() {
        String intestazione = "giornata;squadra1;squadra2;gol1;gol2\n";
        String dati = "";
        for (int i = 0; i < listapartite.size(); i++) {
            Partita pt = listapartite.get(i);
            String riga = pt.getCSVPartita();
            dati += riga;
        }
        String txfile = intestazione + dati;
        try {
            File fi = new File("");
            String pf = fi.getCanonicalPath();
            FileWriter myWriter = new FileWriter(pf + "/Calcio/partite.csv");
            myWriter.write(txfile);
            myWriter.close();
        } catch (Exception e) {
            lblMsg.setText("Errore di scrittura file partite.csv");
        }
    }

    private void salvaCSVClass() {
        String intestazione = "squadra;punteggio\n";
        String dati = "";
        for (int i = 0; i < classifica.size(); i++) {
            Classifica cl = classifica.get(i);
            String riga = cl.getCSVClass();
            dati += riga;
        }
        String txfile = intestazione + dati;
        try {
            File fi = new File("");
            String pf = fi.getCanonicalPath();
            FileWriter myWriter = new FileWriter(pf + "/Calcio/classifica.csv");
            myWriter.write(txfile);
            myWriter.close();
        } catch (Exception e) {
            lblMsg.setText("Errore di scrittura file classifica.csv");
        }
    }

    private boolean verifica(String nomesq1, String nomesq2) {
        boolean ris = false;
        for (Squadra sq : listasquadre) {
            if (nomesq1.equals(sq.getNome())) {
                for (Squadra sq2 : listasquadre) {
                    if (nomesq2.equals(sq2.getNome())) {
                        ris = true;
                    }

                }
            }
        }
        return ris;

    }

